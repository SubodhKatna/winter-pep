// Assignment Operators in JavaScript

let x = 10;
console.log("Initial value of x:", x);

// Assignment (=)
x = 5;
console.log("Assignment (x = 5):", x); // Output: 5

// Add and Assign (+=)
x += 3; // Equivalent to x = x + 3
console.log("Add and Assign (x += 3):", x); // Output: 8

// Subtract and Assign (-=)
x -= 2; // Equivalent to x = x - 2
console.log("Subtract and Assign (x -= 2):", x); // Output: 6

// Multiply and Assign (*=)
x *= 4; // Equivalent to x = x * 4
console.log("Multiply and Assign (x *= 4):", x); // Output: 24

// Divide and Assign (/=)
x /= 6; // Equivalent to x = x / 6
console.log("Divide and Assign (x /= 6):", x); // Output: 4

// Modulus and Assign (%=)
x %= 3; // Equivalent to x = x % 3
console.log("Modulus and Assign (x %= 3):", x); // Output: 1

// Exponential and Assign (**=)
x **= 2; // Equivalent to x = x ** 2
console.log("Exponential and Assign (x **= 2):", x); // Output: 1
