// Arithmetic Operators in JavaScript

let num1 = 10;
let num2 = 3;

console.log("Numbers:", num1, "and", num2);

// Addition (+)
let sum = num1 + num2;
console.log("Addition (num1 + num2):", sum); // Output: 13

// Subtraction (-)
let difference = num1 - num2;
console.log("Subtraction (num1 - num2):", difference); // Output: 7

// Multiplication (*)
let product = num1 * num2;
console.log("Multiplication (num1 * num2):", product); // Output: 30

// Division (/)
let quotient = num1 / num2;
console.log("Division (num1 / num2):", quotient); // Output: 3.3333...

// Modulus (Remainder) (%)
let remainder = num1 % num2;
console.log("Modulus (num1 % num2):", remainder); // Output: 1

// Exponentiation (**)
let power = num1 ** num2;
console.log("Exponentiation (num1 ** num2):", power); // Output: 1000

// Increment (++)
let increment = num1;
increment++;
console.log("Increment (num1++):", increment); // Output: 11

// Decrement (--)
let decrement = num2;
decrement--;
console.log("Decrement (num2--):", decrement); // Output: 2
