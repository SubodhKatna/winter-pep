// Ternary Operator Example

let age = 20;

// Check if the person is eligible to vote
let canVote = age >= 18 ? "Eligible to vote" : "Not eligible to vote";
console.log(canVote); // Output: "Eligible to vote"

// Assigning a value based on a condition
let isEven = (age % 2 === 0) ? "Even" : "Odd";
console.log(isEven); // Output: "Even"

// Using ternary operator for a more complex condition
let result = (age > 18 && age < 60) ? "Adult" : "Not Adult";
console.log(result); // Output: "Adult"
